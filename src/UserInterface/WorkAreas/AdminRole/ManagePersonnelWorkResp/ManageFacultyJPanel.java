    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (selectedFaculty == null) {
            JOptionPane.showMessageDialog(this, "Please select a faculty member to update.");
            return;
        }

        String facultyId = txtFacultyID.getText().trim();
        String name = txtName.getText().trim();

        if (facultyId.isBlank() || name.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter both Faculty ID and name.");
            return;
        }

        FacultyProfile existing = business.getFacultyDirectory().findFaculty(facultyId);

        if (existing != null && existing != selectedFaculty) {
            JOptionPane.showMessageDialog(this, "Another faculty member already has this ID.");
            return;
        }

        Person person = selectedFaculty.getPerson();
        person.setPersonId(facultyId);
        person.setFirstName(name);

        JOptionPane.showMessageDialog(this, "Faculty member updated successfully.");

        clearFields();
        selectedFaculty = null;
        refreshTable();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void tblFacultyMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFacultyMousePressed
        // Extracts the row (uaser account) in the table that is selected by the user
        int selectedRow = tblFaculty.getSelectedRow();

        if (selectedRow < 0) {
            return;
        }

        String facultyId = tblFaculty.getValueAt(selectedRow, 0).toString();

        selectedFaculty = business.getFacultyDirectory().findFaculty(facultyId);

        if (selectedFaculty == null) {
            JOptionPane.showMessageDialog(this, "Could not find selected faculty member.");
            return;
        }

        Person person = selectedFaculty.getPerson();
        txtFacultyID.setText(person.getPersonId());
        txtName.setText(person.getFirstName());
    }//GEN-LAST:event_tblFacultyMousePressed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String facultyId = txtFacultyID.getText().trim();
        String name = txtName.getText().trim();

        if (facultyId.isBlank() || name.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter both Faculty ID and name.");
            return;
        }

        if (business.getFacultyDirectory().findFaculty(facultyId) != null) {
            JOptionPane.showMessageDialog(this, "A faculty member with this ID already exists.");
            return;
        }

        Person person = business.getPersonDirectory().newPerson(facultyId);
        person.setFirstName(name);

        business.getFacultyDirectory().newFacultyProfile(person);

        JOptionPane.showMessageDialog(this, "Faculty member added successfully.");

        clearFields();
        selectedFaculty = null;
        refreshTable();
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (selectedFaculty == null) {
            JOptionPane.showMessageDialog(this, "Please select a faculty member to delete.");
            return;
        }

        Person person = selectedFaculty.getPerson();

        UserAccount account = business.getUserAccountDirectory()
                .findUserAccount(person.getPersonId());

        if (account != null) {
            JOptionPane.showMessageDialog(this,
                    "This faculty member already has a user account. Delete the account first.");
            return;
        }

        int result = JOptionPane.showConfirmDialog(
                this,
                "Are you sure you want to delete this faculty member?",
                "Confirm Delete",
                JOptionPane.YES_NO_OPTION
        );

        if (result != JOptionPane.YES_OPTION) {
            return;
        }

        business.getFacultyDirectory().removeFacultyProfile(selectedFaculty);
        business.getPersonDirectory().removePerson(person);

        JOptionPane.showMessageDialog(this, "Faculty member deleted successfully.");

        clearFields();
        selectedFaculty = null;
        refreshTable();
    }//GEN-LAST:event_btnDeleteActionPerformed


