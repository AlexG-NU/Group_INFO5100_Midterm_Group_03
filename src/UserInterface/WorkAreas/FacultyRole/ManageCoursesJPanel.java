    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) CourseTable.getModel();
        model.setRowCount(0);
        
        for (Course c : courseCatalog.getCourseList()){
            Object[] row = new Object [4];
            row[0]= c.getCOurseNumber();
            row[1]= c.getName();
            row[2]= c.getCoursePrice();
            row[3]= c.getCredits();
            model.addRow(row);
        }
    }
    
